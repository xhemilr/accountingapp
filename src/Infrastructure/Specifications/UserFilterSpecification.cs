using AccountingApp.Application.Specifications.Base;
using AccountingApp.Infrastructure.Models.Identity;

namespace AccountingApp.Infrastructure.Specifications
{
    public class UserFilterSpecification : HeroSpecification<AccountingAppUser>
    {
        public UserFilterSpecification(string searchString)
        {
            if (!string.IsNullOrEmpty(searchString))
            {
                Criteria = p => p.FirstName.Contains(searchString) || p.LastName.Contains(searchString) || p.Email.Contains(searchString) || p.PhoneNumber.Contains(searchString) || p.UserName.Contains(searchString);
            }
            else
            {
                Criteria = p => true;
            }
        }
    }
}